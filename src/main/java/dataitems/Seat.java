package dataitems;

/**
 *
 * @author riverlin
 */
public class Seat {
    private int x;
    private int y;
    private boolean z;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public boolean isZ() {
        return z;
    }

    public void setZ(boolean z) {
        this.z = z;
    }
    
}
